// Contact Form Functions
function submitForm(event) {
    event.preventDefault();
    
    // Get form data
    const formData = new FormData(event.target);
    const data = Object.fromEntries(formData);
    
    // Basic validation
    if (!data.firstName || !data.lastName || !data.email || !data.subject || !data.message) {
        showNotification(currentLanguage === 'ar' ? 'يرجى ملء جميع الحقول المطلوبة' : 'Please fill all required fields', 'error');
        return;
    }
    
    if (!data.privacy) {
        showNotification(currentLanguage === 'ar' ? 'يرجى الموافقة على سياسة الخصوصية' : 'Please agree to the privacy policy', 'error');
        return;
    }
    
    // Simulate form submission
    showNotification(currentLanguage === 'ar' ? 'تم إرسال رسالتك بنجاح، سنتواصل معك قريباً' : 'Your message has been sent successfully, we will contact you soon', 'success');
    
    // Reset form
    event.target.reset();
}

// Close modals when pressing Escape key
document.addEventListener('keydown', function(e) {
    if (e.key === 'Escape') {
        const modals = document.querySelectorAll('.modal');
        modals.forEach(modal => {
            modal.style.display = 'none';
        });
        
        const accessibilityPanel = document.getElementById('accessibility-panel');
        if (accessibilityPanel && accessibilityPanel.classList.contains('active')) {
            toggleAccessibility();
        }
    }
});

